    public String PlacaBase;
    public String Monitor;
    public String Ram;
    public Double Grafica;

    public String getFullName() {
        return Procesador + PlacaBase + Monitor + Ram + Grafica;
    }

    public String getProcesador() {
        return Procesador;
    }

    public String getPlacaBase() {
        return PlacaBase;
    }

    public String getMonitor() {
        return Monitor;
    }

    public String getRam() {
        return Ram;
    }

    public Double getGrafica() {
        return Grafica;
    }
